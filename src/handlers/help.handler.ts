import { Context, InlineKeyboard } from "grammy";

export const help = async (ctx: Context) => {
  const keyboard = new InlineKeyboard()
    .text("🎬 Рандомний фільм", "random_movie")
    .row()
    .text("👤 Мій профіль", "profile");

  await ctx.reply(
    `🎬 *Reelix — ваш особистий кінопомічник*\n\n` +
    `*Команди:*\n` +
    `/start — запустити бота\n` +
    `/help — показати цю довідку\n\n` +
    `*Що вміє бот:*\n\n` +
    `🎲 *Рандомний фільм*\n` +
    `Натисніть кнопку і отримайте випадковий фільм з оцінкою, описом та трейлером\n\n` +
    `❤️ *Улюблені*\n` +
    `Зберігайте до 5 фільмів які вам сподобались — знайдете їх у профілі\n\n` +
    `🕐 *Переглянути пізніше*\n` +
    `Відкладіть до 5 фільмів щоб повернутись до них коли буде час\n\n` +
    `👤 *Профіль*\n` +
    `Переглядайте збережені фільми, видаляйте їх або очистіть профіль повністю\n\n` +
    `💡 *Підказка:* щоб додати новий фільм — спочатку видаліть один зі списку, ліміт 5 фільмів на кожну категорію`,
    { parse_mode: "Markdown", reply_markup: keyboard },
  );
};
